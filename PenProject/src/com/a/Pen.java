package com.a;

public class Pen {

	private int NumofRefills;
	private String Colour;
	private String type;

	public void setNumofRefills(int pen) {
		NumofRefills = pen;
	}

	public int getNumoffRefills() {
		return NumofRefills;
	}

	public void setColour(String clr) {
		Colour = clr;
	}

	public String getColour() {
		return Colour;
	}

	public void setType(String ty) {
		type = ty;
	}

	public String getType() {
		return type;
	}

}
